void HAL_RTCEx_RTCEventCallback(RTC_HandleTypeDef *hrtc)
{
		if(hrtc->Instance == RTC)
		{
				rtc_update = true;    //RTC的秒中断到达
			  HAL_RTC_GetDate(hrtc,&get_data,RTC_FORMAT_BIN);
			  HAL_RTC_GetTime(hrtc,&get_time,RTC_FORMAT_BIN);
		}
}



